#!/usr/bin/env node
// stamp-html.mjs — rewrite tracked HTML files to reference .min.* assets
// with a content-hash query string from scripts/build/asset-manifest.json.
//
// Idempotent: re-running produces identical output. Only the content hash
// changes when the underlying asset changes.
//
// Scope: explicit list of HTML files below. Deliberately does NOT touch
// blog-module/blog-entries/**/article.html — those are committed content
// artifacts that will be regenerated on the next blog rebuild via the
// (already-stamped) blog-module/blog/template.html.
//
// Usage:
//   node scripts/build/stamp-html.mjs
//   node scripts/build/stamp-html.mjs --dry   # print planned edits only

import fs from 'node:fs';
import path from 'node:path';
import crypto from 'node:crypto';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const REPO_ROOT = path.resolve(path.dirname(__filename), '..', '..');
const MANIFEST_PATH = path.join(REPO_ROOT, 'scripts', 'build', 'asset-manifest.json');

// HTML files whose asset references we rewrite. Add to this list with care
// — stamping a file with external asset references is fine; stamping a
// content-committed article is NOT (see module header).
const TARGET_HTML = [
    'index.html',
    'offline.html',
    'standings/index.html',
    'blog-module/blog/index.html',
    'blog-module/blog/template.html',
    'housekeeping.html',
    'privacy/privacy.html',
    'privacy/terms.html',
    'generate.html',
    '404.html'
];

// Pages that get the inlined SVG sprite + FA CDN removal (Phase 3b). Only
// pages that actually use fa-* icons need this; offline.html + 404.html have
// none, so they're excluded.
const SPRITE_TARGETS = new Set([
    'index.html',
    'standings/index.html',
    'blog-module/blog/index.html',
    'blog-module/blog/template.html',
    'housekeeping.html',
    'privacy/privacy.html',
    'privacy/terms.html',
    'generate.html'
]);

const SPRITE_SOURCE = 'images/icons/sprite.svg';
const SPRITE_BEGIN = '<!-- f1s:icon-sprite:begin -->';
const SPRITE_END   = '<!-- f1s:icon-sprite:end -->';

// HTML files that get the critical-CSS treatment (inlined critical block +
// async-preload for local stylesheets). Excluded: offline.html and 404.html
// which already inline all their CSS and have no external refs.
const CRITICAL_TARGETS = new Set([
    'index.html',
    'standings/index.html',
    'blog-module/blog/index.html',
    'blog-module/blog/template.html'
]);

// Source path of the hand-crafted critical block (resolved against manifest).
const CRITICAL_SOURCE = 'styles/critical-common.css';

// Marker comments around the injected <style> block. Idempotent: the block
// is replaced in place on every run so re-stamps produce stable output.
const CRITICAL_BEGIN = '<!-- f1s:critical-css:begin -->';
const CRITICAL_END   = '<!-- f1s:critical-css:end -->';

// Self-hosted font bundle (generated by download-fonts.mjs). Replaces every
// fonts.googleapis.com <link> reference and is inserted into the preload
// section for the primary weight.
const FONTS_SOURCE = 'styles/fonts.css';

// Primary font weight to preload per page template. Picked to match the
// dominant above-the-fold text (hero h1 + body). Path is relative to the
// repo root; stamp-html looks it up in assets/fonts/ literally.
const FONT_PRELOADS = {
    'index.html': [
        'assets/fonts/roboto-400.woff2',
        'assets/fonts/roboto-700.woff2'
    ],
    'standings/index.html': [
        'assets/fonts/roboto-400.woff2',
        'assets/fonts/outfit-700.woff2'
    ],
    'blog-module/blog/index.html': [
        'assets/fonts/dm-sans-400.woff2',
        'assets/fonts/outfit-700.woff2'
    ],
    'blog-module/blog/template.html': [
        'assets/fonts/dm-sans-400.woff2',
        'assets/fonts/outfit-700.woff2'
    ]
};

// Marker comments around the font preload block. Idempotent replacement.
const FONTS_BEGIN = '<!-- f1s:fonts-preload:begin -->';
const FONTS_END   = '<!-- f1s:fonts-preload:end -->';

function loadManifest() {
    if (!fs.existsSync(MANIFEST_PATH)) {
        console.error(`✗ manifest missing: ${MANIFEST_PATH}\n  run: npm run build:assets`);
        process.exit(2);
    }
    const payload = JSON.parse(fs.readFileSync(MANIFEST_PATH, 'utf8'));
    return payload.files || {};
}

function escapeRegex(s) {
    return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

// Match every way a ref to sourceRel might appear in an HTML attribute:
//   href="/styles.css"                    (root-absolute)
//   href="styles/shared-nav.css"          (relative with subpath)
//   href="standings.css"                  (bare basename, usually same-dir)
//   href="styles.css?v=anything"          (existing query string, any form)
// The minified replacement mirrors the same prefix form to keep the page
// happy regardless of its own location in the tree.
function buildPatternsFor(sourceRel) {
    const basename = path.basename(sourceRel);                  // e.g. styles.css
    const minBasename = path.basename(sourceRel).replace(/\.(css|js)$/, '.min.$1');
    const minRel = sourceRel.replace(/\.(css|js)$/, '.min.$1');
    const refs = [];
    // Root-absolute form: "/styles/shared-nav.css"
    refs.push({ from: '/' + sourceRel, to: '/' + minRel });
    // Relative-with-subpath form: "styles/shared-nav.css"
    if (sourceRel.includes('/')) {
        refs.push({ from: sourceRel, to: minRel });
    }
    // Bare basename (only if the source has no subpath, else ambiguous)
    if (!sourceRel.includes('/') || sourceRel.split('/').length === 2) {
        refs.push({ from: basename, to: minBasename });
    }
    return refs.map(ref => ({
        regex: new RegExp(
            `(href|src)=("|')(` + escapeRegex(ref.from) + `)(\\?[^"']*)?(\\2)`,
            'g'
        ),
        toRef: ref.to,
        source: sourceRel
    }));
}

function buildPatternsFromManifest(manifest) {
    const out = [];
    for (const [sourceRel, info] of Object.entries(manifest)) {
        const patterns = buildPatternsFor(sourceRel);
        for (const p of patterns) {
            out.push({ ...p, hash: info.hash });
        }
    }
    // Sort longer `from` refs first so "/scripts/perf/web-vitals-beacon.js"
    // wins before a generic "web-vitals-beacon.js" rule ever runs.
    out.sort((a, b) => b.regex.source.length - a.regex.source.length);
    return out;
}

function rewrite(html, patterns) {
    let result = html;
    const hits = [];
    for (const p of patterns) {
        result = result.replace(p.regex, (match, attr, q) => {
            const stamped = `${attr}=${q}${p.toRef}?v=${p.hash}${q}`;
            if (match !== stamped) {
                hits.push({ source: p.source, from: match, to: stamped });
            }
            return stamped;
        });
    }
    return { result, hits };
}

// Load the minified critical CSS from disk so it can be inlined verbatim.
function loadCriticalCss(manifest) {
    const info = manifest[CRITICAL_SOURCE];
    if (!info) {
        throw new Error(`manifest missing entry for ${CRITICAL_SOURCE} — run build:assets:minify`);
    }
    const abs = path.join(REPO_ROOT, info.min);
    if (!fs.existsSync(abs)) {
        throw new Error(`critical CSS build missing at ${abs} — run build:assets:minify`);
    }
    return {
        css: fs.readFileSync(abs, 'utf8'),
        hash: info.hash,
        bytes: info.bytes
    };
}

// Replace the existing critical block (if any) or inject a new one right
// before the first <link rel="stylesheet"> inside <head>. Idempotent.
function injectCritical(html, critical) {
    const block =
        `${CRITICAL_BEGIN}\n` +
        `    <style id="f1s-critical" data-hash="${critical.hash}">${critical.css.trimEnd()}</style>\n` +
        `    ${CRITICAL_END}`;

    const existing = new RegExp(
        escapeRegex(CRITICAL_BEGIN) + '[\\s\\S]*?' + escapeRegex(CRITICAL_END),
        'g'
    );
    if (existing.test(html)) {
        const replaced = html.replace(existing, block);
        return { result: replaced, injected: false, replaced: true };
    }

    // Insert before first <link rel="stylesheet" ...> that points to a local
    // stylesheet (skips bootstrap CDN etc. by requiring no :// in href value,
    // but simplest: just anchor on the first local .min.css link — which
    // post-Phase-1 always exists on these pages).
    const anchor = /([ \t]*)<link rel="stylesheet" href="([^"]*\.min\.css)/;
    const m = html.match(anchor);
    if (!m) {
        return { result: html, injected: false, replaced: false };
    }
    const indent = m[1] || '    ';
    const insertion = `${indent}${block}\n`;
    const result = html.slice(0, m.index) + insertion + html.slice(m.index);
    return { result, injected: true, replaced: false };
}

// Convert a local stylesheet link to the async-preload pattern. External
// hrefs (bootstrap CDN, google fonts, font-awesome CDN) are left alone.
// Skips links that are already in preload form or in <noscript>.
//
// Before:  <link rel="stylesheet" href="/styles.min.css?v=abc123">
// After:   <link rel="preload" as="style" href="/styles.min.css?v=abc123"
//              onload="this.onload=null;this.rel='stylesheet'">
//          <noscript><link rel="stylesheet" href="/styles.min.css?v=abc123"></noscript>
// Swap Google Fonts markup for self-hosted equivalents:
//  1. Drop preconnect links to fonts.googleapis.com + fonts.gstatic.com.
//  2. Replace each <link href="https://fonts.googleapis.com/css2?..."> with
//     a <link rel="stylesheet" href="/styles/fonts.min.css?v=<hash>">
//     (which the later asyncify pass converts to rel=preload).
//  3. Emit a marker-bracketed block of <link rel="preload" as="font">
//     pointing at the primary weights for this page template.
//
// Idempotent: running twice produces the same output.
function swapFonts(html, relPath, fontsInfo, manifest) {
    const swaps = { preconnectDropped: 0, googleLinksSwapped: 0, preloadsInjected: 0 };

    // 1. Drop preconnect to Google Fonts domains.
    html = html.replace(
        /^[ \t]*<link\s+rel="preconnect"\s+href="https:\/\/fonts\.(googleapis|gstatic)\.com"[^>]*>\n/gm,
        () => { swaps.preconnectDropped++; return ''; }
    );

    // 2. Replace Google Fonts <link>s (all three forms: plain stylesheet,
    //    rel=preload async form, and <noscript> fallback) with a single
    //    local fonts.min.css link. Dedupe: first occurrence becomes the
    //    local link, later ones are dropped.
    let emitted = false;
    const fontsLocalHref = `/${fontsInfo.min}?v=${fontsInfo.hash}`;

    const googleFontsLineRe = new RegExp(
        [
            // a) <link rel="stylesheet" href="https://fonts.googleapis.com/...">
            //    or the href-first variant.
            `^([ \\t]*)<link\\s+(?:href="https:\\/\\/fonts\\.googleapis\\.com\\/[^"]+"\\s+rel="stylesheet"|rel="stylesheet"\\s+href="https:\\/\\/fonts\\.googleapis\\.com\\/[^"]+")>\\n`,
            // b) <link rel="preload" href="https://fonts.googleapis.com/..." as="style" onload="...">
            `^([ \\t]*)<link\\s+rel="preload"\\s+href="https:\\/\\/fonts\\.googleapis\\.com\\/[^"]+"\\s+as="style"[^>]*>\\n`,
            // c) <noscript><link href="https://fonts.googleapis.com/..." rel="stylesheet"></noscript>
            `^([ \\t]*)<noscript><link[^>]*https:\\/\\/fonts\\.googleapis\\.com\\/[^>]*><\\/noscript>\\n`
        ].join('|'),
        'gm'
    );

    html = html.replace(googleFontsLineRe, (_m, i1, i2, i3) => {
        swaps.googleLinksSwapped++;
        const indent = i1 || i2 || i3 || '    ';
        if (emitted) return '';
        emitted = true;
        return `${indent}<link rel="stylesheet" href="${fontsLocalHref}">\n`;
    });

    // 3. Inject/refresh preload block for primary font weights.
    const preloadList = FONT_PRELOADS[relPath] || [];
    if (preloadList.length) {
        const lines = [FONTS_BEGIN];
        for (const rel of preloadList) {
            const abs = path.join(REPO_ROOT, rel);
            if (!fs.existsSync(abs)) continue;
            const hash = sha256Short(rel);
            lines.push(`    <link rel="preload" as="font" type="font/woff2" href="/${rel}?v=${hash}" crossorigin>`);
        }
        lines.push(`    ${FONTS_END}`);
        const block = lines.join('\n');

        const existing = new RegExp(
            escapeRegex(FONTS_BEGIN) + '[\\s\\S]*?' + escapeRegex(FONTS_END),
            'g'
        );
        if (existing.test(html)) {
            html = html.replace(existing, block);
            swaps.preloadsInjected = preloadList.length;
        } else {
            // Insert right before the critical-CSS block (or before first
            // local .min.css link as fallback). Anchor to the leading-
            // whitespace + begin-marker so we can reuse its indent.
            const anchor = new RegExp(`^([ \\t]*)${escapeRegex(CRITICAL_BEGIN)}`, 'm');
            const mAnchor = html.match(anchor);
            if (mAnchor) {
                const indent = mAnchor[1] || '    ';
                html = html.replace(
                    anchor,
                    `${indent}${block}\n${indent}${CRITICAL_BEGIN}`
                );
            } else {
                const fallback = /([ \t]*)<link rel="stylesheet" href="[^"]*\.min\.css/;
                const m = html.match(fallback);
                if (m) {
                    const indent = m[1] || '    ';
                    html = html.slice(0, m.index) + `${indent}${block}\n` + html.slice(m.index);
                }
            }
            swaps.preloadsInjected = preloadList.length;
        }
    }

    return { result: html, swaps };
}

// Small sha256-short for font files (content hash based on path, since
// fonts are woff2 binaries and we don't re-read them on every stamp).
function sha256Short(input) {
    const abs = path.join(REPO_ROOT, input);
    const buf = fs.existsSync(abs) ? fs.readFileSync(abs) : Buffer.from(input);
    return crypto.createHash('sha256').update(buf).digest('hex').slice(0, 8);
}

// Drop the Font Awesome CDN references. Phase 3b replaces them with an
// inlined SVG sprite, so every cdnjs FA <link> — plain stylesheet, media-
// swap async form, and the <noscript> fallback — becomes dead weight.
// Also drops the sibling `<link rel="dns-prefetch" href="https://cdnjs…">`
// since cdnjs is only used for FA on this site.
function dropFontAwesomeCdn(html) {
    let dropped = 0;
    const before = html;

    // Match any <link …cdnjs.cloudflare.com…font-awesome…> whole line
    // (plain stylesheet, async with media=print onload=…, or standalone).
    html = html.replace(
        /^[ \t]*<link\s+[^>]*cdnjs\.cloudflare\.com\/ajax\/libs\/font-awesome\/[^>]*>\n/gm,
        () => { dropped++; return ''; }
    );
    // Match the <noscript> fallback form as a whole line.
    html = html.replace(
        /^[ \t]*<noscript><link[^>]*cdnjs\.cloudflare\.com\/ajax\/libs\/font-awesome\/[^>]*><\/noscript>\n/gm,
        () => { dropped++; return ''; }
    );
    // Drop the dns-prefetch line for cdnjs (cdnjs is only used for FA here).
    html = html.replace(
        /^[ \t]*<link\s+rel="dns-prefetch"\s+href="https:\/\/cdnjs\.cloudflare\.com"[^>]*>\n/gm,
        () => { dropped++; return ''; }
    );

    return { result: html, dropped, changed: html !== before };
}

// Inline the SVG sprite inside <body> so every `<svg><use href="#fa-*"/>`
// resolves without a network hop. Idempotent via marker-bracketed block;
// the stored `data-hash` lets browsers (and us) see when the sprite churned.
function injectSprite(html, spriteInfo) {
    const block =
        `${SPRITE_BEGIN}\n` +
        `<div hidden data-hash="${spriteInfo.hash}">${spriteInfo.svg}</div>\n` +
        `${SPRITE_END}`;

    const existing = new RegExp(
        escapeRegex(SPRITE_BEGIN) + '[\\s\\S]*?' + escapeRegex(SPRITE_END),
        'g'
    );
    if (existing.test(html)) {
        const replaced = html.replace(existing, block);
        return { result: replaced, injected: false, replaced: true };
    }

    // Insert immediately after the opening <body …> tag.
    const anchor = /<body\b[^>]*>\n?/;
    const m = html.match(anchor);
    if (!m) return { result: html, injected: false, replaced: false };
    const insertAt = m.index + m[0].length;
    const result = html.slice(0, insertAt) + block + '\n' + html.slice(insertAt);
    return { result, injected: true, replaced: false };
}

// Load the sprite once per stamp run. Stripped of the leading <?xml?> + the
// outer comment so the inlined form reads as ordinary markup inside <body>.
function loadSprite() {
    const abs = path.join(REPO_ROOT, SPRITE_SOURCE);
    if (!fs.existsSync(abs)) {
        throw new Error(`sprite missing at ${abs} — run: npm run build:icons`);
    }
    const raw = fs.readFileSync(abs, 'utf8');
    const hash = crypto.createHash('sha256').update(raw).digest('hex').slice(0, 8);
    // Trim the XML prolog + generator comment for cleaner HTML output;
    // browsers don't need xmlns on a child <svg> inside <body>.
    const svg = raw
        .replace(/^<\?xml[^?]*\?>\s*/, '')
        .replace(/^<!--[\s\S]*?-->\s*/, '')
        .trim();
    return { svg, hash };
}

function asyncifyStylesheets(html) {
    const conversions = [];

    // Match <link rel="stylesheet" ...> where href points to a .min.css (local
    // post-stamp form). href may appear before or after rel. The attribute
    // order in stamped files is `rel="stylesheet" href="..."` so that's what
    // we optimize for. Exclude lines inside <noscript> via a lookbehind-free
    // check: require the match to start at an indent, not immediately after
    // <noscript>.
    const re = /^([ \t]*)<link rel="stylesheet" href="([^"]*\.min\.css\?v=[a-f0-9]+)">/gm;

    const result = html.replace(re, (match, indent, href, offset) => {
        // Skip if the match is inside a <noscript>…</noscript> block. We look
        // for the nearest <noscript> tag before `offset` without a closing
        // </noscript> between it and the match.
        const before = html.slice(0, offset);
        const lastOpen = before.lastIndexOf('<noscript');
        const lastClose = before.lastIndexOf('</noscript>');
        if (lastOpen > lastClose) return match;

        conversions.push(href);
        return (
            `${indent}<link rel="preload" as="style" href="${href}" onload="this.onload=null;this.rel='stylesheet'">\n` +
            `${indent}<noscript><link rel="stylesheet" href="${href}"></noscript>`
        );
    });

    return { result, conversions };
}

function main() {
    const manifest = loadManifest();
    const patterns = buildPatternsFromManifest(manifest);
    const critical = loadCriticalCss(manifest);
    const fontsInfo = manifest[FONTS_SOURCE];
    if (!fontsInfo) {
        throw new Error(`manifest missing entry for ${FONTS_SOURCE} — run build:fonts then build:assets:minify`);
    }
    const sprite = loadSprite();
    const dry = process.argv.includes('--dry');
    let totalHits = 0;
    let totalCriticalOps = 0;
    let totalAsyncified = 0;
    let totalFontSwaps = 0;
    let totalPreconnectDrops = 0;
    let totalSpriteOps = 0;
    let totalFaCdnDrops = 0;

    for (const rel of TARGET_HTML) {
        const abs = path.join(REPO_ROOT, rel);
        if (!fs.existsSync(abs)) {
            console.warn(`skip (missing): ${rel}`);
            continue;
        }
        const original = fs.readFileSync(abs, 'utf8');

        // 1) stamp local asset refs with content-hash query strings
        let { result, hits } = rewrite(original, patterns);

        // 2) swap Google Fonts → self-hosted (only on pages that had them)
        const fontSwap = swapFonts(result, rel, fontsInfo, manifest);
        result = fontSwap.result;
        totalFontSwaps += fontSwap.swaps.googleLinksSwapped;
        totalPreconnectDrops += fontSwap.swaps.preconnectDropped;

        // 2b) Phase 3b: drop FA CDN <link>s + inline sprite (only on pages
        //     that use icons; offline/404 have none).
        let faDropNote = '';
        let spriteNote = '';
        if (SPRITE_TARGETS.has(rel)) {
            const cdnDrop = dropFontAwesomeCdn(result);
            result = cdnDrop.result;
            totalFaCdnDrops += cdnDrop.dropped;
            if (cdnDrop.dropped) faDropNote = `${cdnDrop.dropped} FA CDN drop`;

            const inj = injectSprite(result, sprite);
            result = inj.result;
            if (inj.injected) { spriteNote = 'sprite injected'; totalSpriteOps++; }
            else if (inj.replaced) { spriteNote = 'sprite refreshed'; totalSpriteOps++; }
        }

        // 3) inject/refresh critical-CSS block + asyncify local stylesheets
        let criticalNote = '';
        let conversions = [];
        if (CRITICAL_TARGETS.has(rel)) {
            const inj = injectCritical(result, critical);
            result = inj.result;
            if (inj.injected) { criticalNote = 'injected'; totalCriticalOps++; }
            else if (inj.replaced) { criticalNote = 'refreshed'; totalCriticalOps++; }
            else criticalNote = 'skipped (no anchor)';

            const out = asyncifyStylesheets(result);
            result = out.result;
            conversions = out.conversions;
            totalAsyncified += conversions.length;
        }

        totalHits += hits.length;
        const changed = result !== original;
        if (!changed) {
            console.log(`· ${rel}  (no changes)`);
            continue;
        }
        const parts = [];
        if (hits.length) parts.push(`${hits.length} stamp(s)`);
        if (criticalNote && criticalNote !== 'skipped (no anchor)') parts.push(`critical ${criticalNote}`);
        if (conversions.length) parts.push(`${conversions.length} async-preload`);
        if (fontSwap.swaps.googleLinksSwapped) parts.push(`${fontSwap.swaps.googleLinksSwapped} font swap`);
        if (fontSwap.swaps.preconnectDropped) parts.push(`${fontSwap.swaps.preconnectDropped} preconnect drop`);
        if (fontSwap.swaps.preloadsInjected) parts.push(`${fontSwap.swaps.preloadsInjected} font preload`);
        if (faDropNote) parts.push(faDropNote);
        if (spriteNote) parts.push(spriteNote);
        console.log(`\n${rel}  →  ${parts.join(', ') || 'updated'}`);
        for (const h of hits) {
            console.log(`    ${h.from}\n  → ${h.to}`);
        }
        for (const href of conversions) {
            console.log(`    async: ${href}`);
        }
        if (!dry) {
            fs.writeFileSync(abs, result, 'utf8');
        }
    }

    if (dry) {
        console.log(
            `\n(dry-run) ${totalHits} rewrite(s), ${totalCriticalOps} critical op(s), ` +
            `${totalAsyncified} async-preload conversion(s), ${totalFontSwaps} font swap(s), ` +
            `${totalPreconnectDrops} preconnect drop(s), ${totalSpriteOps} sprite op(s), ` +
            `${totalFaCdnDrops} FA CDN drop(s) planned.`
        );
    } else {
        console.log(
            `\n✓ stamped ${totalHits} reference(s), ${totalCriticalOps} critical block(s), ` +
            `${totalAsyncified} stylesheet(s) async-preloaded, ${totalFontSwaps} font swap(s), ` +
            `${totalPreconnectDrops} preconnect(s) dropped, ${totalSpriteOps} sprite op(s), ` +
            `${totalFaCdnDrops} FA CDN drop(s) across ${TARGET_HTML.length} file(s).`
        );
    }
}

main();
