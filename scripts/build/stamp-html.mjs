function loadManifest() {
    if (!fs.existsSync(MANIFEST_PATH)) {
        console.error(`✗ manifest missing: ${MANIFEST_PATH}\n  run: npm run build:assets`);
        process.exit(2);
    }
    const payload = JSON.parse(fs.readFileSync(MANIFEST_PATH, 'utf8'));
    return payload.files || {};
}

function escapeRegex(s) {
    return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

// Match every way a ref to sourceRel might appear in an HTML attribute:
//   href="/styles.css"                    (root-absolute)
//   href="styles/shared-nav.css"          (relative with subpath)
//   href="standings.css"                  (bare basename, usually same-dir)
//   href="styles.css?v=anything"          (existing query string, any form)
// The minified replacement mirrors the same prefix form to keep the page
// happy regardless of its own location in the tree.
function buildPatternsFor(sourceRel) {
    const basename = path.basename(sourceRel);                  // e.g. styles.css
    const minBasename = path.basename(sourceRel).replace(/\.(css|js)$/, '.min.$1');
    const minRel = sourceRel.replace(/\.(css|js)$/, '.min.$1');
    const refs = [];
    const seen = new Set();

    function addRef(from, to) {
        const key = `${from}=>${to}`;
        if (seen.has(key)) return;
        refs.push({ from, to });
        seen.add(key);
    }

    // Root-absolute form: "/styles/shared-nav.css"
    addRef('/' + sourceRel, '/' + minRel);
    // Root-absolute minified form already present from a previous stamp.
    addRef('/' + minRel, '/' + minRel);

    // Relative-with-subpath form: "styles/shared-nav.css"
    if (sourceRel.includes('/')) {
        addRef(sourceRel, minRel);
        addRef(minRel, minRel);
    }

    // Bare basename (only if the source has no subpath, else ambiguous)
    if (!sourceRel.includes('/') || sourceRel.split('/').length === 2) {
        addRef(basename, minBasename);
        addRef(minBasename, minBasename);
    }

    return refs.map(ref => ({
        regex: new RegExp(
            `(href|src)=("|')(` + escapeRegex(ref.from) + `)(\\?[^"']*)?(\\2)`,
            'g'
        ),
        toRef: ref.to,
        source: sourceRel
    }));
}

function buildPatternsFromManifest(manifest) {
    const out = [];
    for (const [sourceRel, info] of Object.entries(manifest)) {
        const patterns = buildPatternsFor(sourceRel);
        for (const p of patterns) {
            out.push({ ...p, hash: info.hash });
        }
    }
    // Sort longer `from` refs first so "/scripts/perf/web-vitals-beacon.js"
    // wins before a generic "web-vitals-beacon.js" rule ever runs.
    out.sort((a, b) => b.regex.source.length - a.regex.source.length);
    return out;
}

function rewrite(html, patterns) {
    let result = html;
    const hits = [];
    for (const p of patterns) {
        result = result.replace(p.regex, (match, attr, q) => {
            const stamped = `${attr}=${q}${p.toRef}?v=${p.hash}${q}`;
            if (match !== stamped) {
                hits.push({ source: p.source, from: match, to: stamped });
            }
            return stamped;
        });
    }
    return { result, hits };
}

// Load the minified critical CSS from disk so it can be inlined verbatim.
function loadCriticalCss(manifest) {
    const info = manifest[CRITICAL_SOURCE];
    if (!info) {
        throw new Error(`manifest missing entry for ${CRITICAL_SOURCE} — run build:assets:minify`);
    }
    const abs = path.join(REPO_ROOT, info.min);
    if (!fs.existsSync(abs)) {
        throw new Error(`critical CSS build missing at ${abs} — run build:assets:minify`);
    }
    return {
        css: fs.readFileSync(abs, 'utf8'),
        hash: info.hash,
        bytes: info.bytes
    };
}

function getTagAttr(tag, name) {
    const match = tag.match(new RegExp(`\\b${name}\\s*=\\s*(["'])(.*?)\\1`, 'i'));
    return match ? match[2] : '';
}

function isLocalHref(href) {
    return href && !/^(?:[a-z][a-z0-9+.-]*:)?\/\//i.test(href);
}

function isLocalStylesheetHref(href) {
    return isLocalHref(href) && /\.css(?:\?[^"'#]*)?$/i.test(href);
}

function isLocalMinStylesheetHref(href) {
    return isLocalHref(href) && /\.min\.css(?:\?[^"'#]*)?$/i.test(href);
}

function isDeferredAuxiliaryStylesheet(href) {
    return /\/styles\/fonts\.min\.css(?:\?|$)|\/styles\/vendor\/bootstrap\.slim\.min\.css(?:\?|$)/i.test(href);
}

function findLocalStylesheetAnchor(html) {
    const linkRe = /<link\b[^>]*>/gi;
    let match;
    let fallback = null;

    while ((match = linkRe.exec(html))) {
        const index = match.index ?? 0;
        if (isInsideNoscript(html, index)) continue;

        const tag = match[0];
        const href = getTagAttr(tag, 'href');
        if (!isLocalMinStylesheetHref(href)) continue;

        const rel = getTagAttr(tag, 'rel').toLowerCase();
        const as = getTagAttr(tag, 'as').toLowerCase();
        const isStylesheet = rel === 'stylesheet' || (rel === 'preload' && as === 'style');
        if (!isStylesheet) continue;

        const lineStart = html.lastIndexOf('\n', index - 1) + 1;
        const indent = html.slice(lineStart, index).match(/^[ \t]*/)?.[0] || '    ';
        const anchor = { index, indent, href, rel };
        if (!isDeferredAuxiliaryStylesheet(href)) {
            return anchor;
        }
        if (!fallback) {
            fallback = anchor;
        }
    }

    return fallback;
}

function normalizeStylesheetNoscriptFallbacks(html) {
    let fixes = 0;
    const result = html.replace(
        /^([ \t]*)<noscript>[ \t]*\n[ \t]*(<link\b[^>]*>)<\/noscript>\s*$/gm,
        (match, indent, linkTag) => {
            const rel = getTagAttr(linkTag, 'rel').toLowerCase();
            const href = getTagAttr(linkTag, 'href');
            if (rel !== 'stylesheet' || !isLocalStylesheetHref(href)) {
                return match;
            }
            fixes++;
            return `${indent}<noscript>${linkTag}</noscript>`;
        }
    );
    return { result, fixes };
}

// Replace the existing critical block (if any) or inject a new one right
// before the first local non-noscript stylesheet anchor inside <head>.
// Idempotent.
function injectCritical(html, critical) {
    const block =
        `${CRITICAL_BEGIN}\n` +
        `    <style id="f1s-critical" data-hash="${critical.hash}">${critical.css.trimEnd()}</style>\n` +
        `    ${CRITICAL_END}`;

    const existing = new RegExp(
        escapeRegex(CRITICAL_BEGIN) + '[\\s\\S]*?' + escapeRegex(CRITICAL_END),
        'g'
    );
    const existingMatch = html.match(existing);
    if (existingMatch) {
        const firstIndex = html.indexOf(existingMatch[0]);
        if (!isInsideNoscript(html, firstIndex)) {
            const replaced = html.replace(existing, block);
            return { result: replaced, injected: false, replaced: true };
        }

        html = html.replace(existing, '');
    }

    // Prefer the main shell stylesheet anchors. On already-asyncified pages
    // that means the first local rel=preload/as=style .min.css outside any
    // <noscript>, not the malformed fallback that older archive pages had.
    const anchor = findLocalStylesheetAnchor(html);
    if (!anchor) {
        return { result: html, injected: false, replaced: false };
    }
    const indent = anchor.indent || '    ';
    const insertion = `${indent}${block}\n`;
    const result = html.slice(0, anchor.index) + insertion + html.slice(anchor.index);
    return { result, injected: true, replaced: false };
}

function isInsideNoscript(html, offset) {
    const before = html.slice(0, offset);
    return before.lastIndexOf('<noscript') > before.lastIndexOf('</noscript>');
}

// Convert a local stylesheet link to the async-preload pattern. External
// hrefs (bootstrap CDN, google fonts, font-awesome CDN) are left alone.
// Skips links that are already in preload form or in <noscript>.
//
// Before:  <link rel="stylesheet" href="/styles.min.css?v=abc123">
// After:   <link rel="preload" as="style" href="/styles.min.css?v=abc123"
//              onload="this.onload=null;this.rel='stylesheet'">
//          <noscript><link rel="stylesheet" href="/styles.min.css?v=abc123"></noscript>
// Swap Google Fonts markup for self-hosted equivalents:
//  1. Drop preconnect links to fonts.googleapis.com + fonts.gstatic.com.
//  2. Replace each <link href="https://fonts.googleapis.com/css2?..."> with
//     a <link rel="stylesheet" href="/styles/fonts.min.css?v=<hash>">
//     (which the later asyncify pass converts to rel=preload).
//  3. Emit a marker-bracketed block of <link rel="preload" as="font">
//     pointing at the primary weights for this page template.
//
// Idempotent: running twice produces the same output.
function swapFonts(html, relPath, fontsInfo, manifest) {
    const swaps = { preconnectDropped: 0, googleLinksSwapped: 0, preloadsInjected: 0 };

    // 1. Drop preconnect to Google Fonts domains.
    html = html.replace(
        /^[ \t]*<link\s+rel="preconnect"\s+href="https:\/\/fonts\.(googleapis|gstatic)\.com"[^>]*>\n/gm,
        () => { swaps.preconnectDropped++; return ''; }
    );

    // 2. Replace Google Fonts <link>s (all three forms: plain stylesheet,
    //    rel=preload async form, and <noscript> fallback) with a single
    //    local fonts.min.css link. Dedupe: first occurrence becomes the
    //    local link, later ones are dropped.
    let emitted = false;
    const fontsLocalHref = `/${fontsInfo.min}?v=${fontsInfo.hash}`;

    const googleFontsLineRe = new RegExp(
        [
            // a) <link rel="stylesheet" href="https://fonts.googleapis.com/...">
            //    or the href-first variant.
            `^([ \\t]*)<link\\s+(?:href="https:\\/\\/fonts\\.googleapis\\.com\\/[^"]+"\\s+rel="stylesheet"|rel="stylesheet"\\s+href="https:\\/\\/fonts\\.googleapis\\.com\\/[^"]+")>\\n`,
            // b) <link rel="preload" href="https://fonts.googleapis.com/..." as="style" onload="...">
            `^([ \\t]*)<link\\s+rel="preload"\\s+href="https:\\/\\/fonts\\.googleapis\\.com\\/[^"]+"\\s+as="style"[^>]*>\\n`,
            // c) <noscript><link href="https://fonts.googleapis.com/..." rel="stylesheet"></noscript>
            `^([ \\t]*)<noscript><link[^>]*https:\\/\\/fonts\\.googleapis\\.com\\/[^>]*><\\/noscript>\\n`
        ].join('|'),
        'gm'
    );

    html = html.replace(googleFontsLineRe, (_m, i1, i2, i3) => {
        swaps.googleLinksSwapped++;
        const indent = i1 || i2 || i3 || '    ';
        if (emitted) return '';
        emitted = true;
        return `${indent}<link rel="stylesheet" href="${fontsLocalHref}">\n`;
    });

    // 3. Inject/refresh preload block for primary font weights.
    const preloadList = FONT_PRELOADS[relPath] || [];
    if (preloadList.length) {
        const lines = [FONTS_BEGIN];
        for (const rel of preloadList) {
            const abs = path.join(REPO_ROOT, rel);
            if (!fs.existsSync(abs)) continue;
            const hash = sha256Short(rel);
            lines.push(`    <link rel="preload" as="font" type="font/woff2" href="/${rel}?v=${hash}" crossorigin>`);
        }
        lines.push(`    ${FONTS_END}`);
        const block = lines.join('\n');

        const existing = new RegExp(
            escapeRegex(FONTS_BEGIN) + '[\\s\\S]*?' + escapeRegex(FONTS_END),
            'g'
        );
        if (existing.test(html)) {
            html = html.replace(existing, block);
            swaps.preloadsInjected = preloadList.length;
        } else {
            // Insert right before the critical-CSS block (or before first
            // local .min.css link as fallback). Anchor to the leading-
            // whitespace + begin-marker so we can reuse its indent.
            const anchor = new RegExp(`^([ \\t]*)${escapeRegex(CRITICAL_BEGIN)}`, 'm');
            const mAnchor = html.match(anchor);
            if (mAnchor) {
                const indent = mAnchor[1] || '    ';
                html = html.replace(
                    anchor,
                    `${indent}${block}\n${indent}${CRITICAL_BEGIN}`
                );
            } else {
                const fallback = /([ \t]*)<link rel="stylesheet" href="[^"]*\.min\.css/;
                const m = html.match(fallback);
                if (m) {
                    const indent = m[1] || '    ';
                    html = html.slice(0, m.index) + `${indent}${block}\n` + html.slice(m.index);
                }
            }
            swaps.preloadsInjected = preloadList.length;
        }
    }

    return { result: html, swaps };
}

function bootstrapPreloadBlock(indent, href) {
    return (
        `${indent}<link rel="preload" as="style" href="${href}" onload="this.onload=null;this.rel='stylesheet'">\n` +
        `${indent}<noscript><link rel="stylesheet" href="${href}"></noscript>\n`
    );
}

function swapBootstrapCdn(html, bootstrapInfo) {
    const swaps = { cssSwapped: 0, cssDropped: 0, jsDropped: 0, preconnectDropped: 0 };
    const localHref = `/${bootstrapInfo.min}?v=${bootstrapInfo.hash}`;
    let emitted = html.includes(localHref);

    const cssLineRe = new RegExp(
        [
            // Plain or preload CDN stylesheet line.
            `^([ \\t]*)<link\\s+[^>]*cdn\\.jsdelivr\\.net\\/npm\\/bootstrap@[^"]+\\/dist\\/css\\/bootstrap\\.min\\.css[^>]*>\\n?`,
            // Noscript fallback line for the same CDN stylesheet.
            `^([ \\t]*)<noscript><link[^>]*cdn\\.jsdelivr\\.net\\/npm\\/bootstrap@[^"]+\\/dist\\/css\\/bootstrap\\.min\\.css[^>]*><\\/noscript>\\n?`
        ].join('|'),
        'gm'
    );

    html = html.replace(cssLineRe, (_m, i1, i2) => {
        const indent = i1 || i2 || '    ';
        if (emitted) {
            swaps.cssDropped++;
            return '';
        }
        emitted = true;
        swaps.cssSwapped++;
        return bootstrapPreloadBlock(indent, localHref);
    });

    html = html.replace(
        /^[ \t]*<script\s+[^>]*cdn\.jsdelivr\.net\/npm\/bootstrap@[^"]+\/dist\/js\/bootstrap(?:\.bundle)?\.min\.js[^>]*><\/script>\n?/gm,
        () => {
            swaps.jsDropped++;
            return '';
        }
    );

    const hasJsdelivrResource = html
        .split('\n')
        .some(line => (
            line.includes('cdn.jsdelivr.net/')
            && !/<link\s+[^>]*rel="preconnect"[^>]*href="https:\/\/cdn\.jsdelivr\.net"[^>]*>/i.test(line)
        ));
    if (!hasJsdelivrResource) {
        html = html.replace(
            /^[ \t]*<link\s+[^>]*rel="preconnect"[^>]*href="https:\/\/cdn\.jsdelivr\.net"[^>]*>\n?/gm,
            () => {
                swaps.preconnectDropped++;
                return '';
            }
        );
    }

    return { result: html, swaps };
}

function listArticleHtml() {
    const root = path.join(REPO_ROOT, ARTICLE_HTML_ROOT);
    const out = [];
    if (!fs.existsSync(root)) return out;

    function walk(dir) {
        for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
            const abs = path.join(dir, entry.name);
            if (entry.isDirectory()) {
                walk(abs);
            } else if (entry.isFile() && entry.name === 'article.html') {
                out.push(path.relative(REPO_ROOT, abs).split(path.sep).join('/'));
            }
        }
    }

    walk(root);
    return out.sort();
}

function stampArticleBootstrap(bootstrapInfo, dry) {
    const totals = { files: 0, cssSwapped: 0, cssDropped: 0, jsDropped: 0, preconnectDropped: 0 };
    for (const rel of listArticleHtml()) {
        const abs = path.join(REPO_ROOT, rel);
        const original = fs.readFileSync(abs, 'utf8');
        const { result, swaps } = swapBootstrapCdn(original, bootstrapInfo);
        if (result === original) continue;

        totals.files++;
        totals.cssSwapped += swaps.cssSwapped;
        totals.cssDropped += swaps.cssDropped;
        totals.jsDropped += swaps.jsDropped;
        totals.preconnectDropped += swaps.preconnectDropped;
        if (!dry) {
            fs.writeFileSync(abs, result, 'utf8');
        }
    }

    return totals;
}

// Small sha256-short for font files (content hash based on path, since
// fonts are woff2 binaries and we don't re-read them on every stamp).
function sha256Short(input) {
    const abs = path.join(REPO_ROOT, input);
    const buf = fs.existsSync(abs) ? fs.readFileSync(abs) : Buffer.from(input);
    return crypto.createHash('sha256').update(buf).digest('hex').slice(0, 8);
}

// Drop the Font Awesome CDN references. Phase 3b replaces them with an
// inlined SVG sprite, so every cdnjs FA <link> — plain stylesheet, media-
// swap async form, and the <noscript> fallback — becomes dead weight.
// Also drops the sibling `<link rel="dns-prefetch" href="https://cdnjs…">`
// since cdnjs is only used for FA on this site.
function dropFontAwesomeCdn(html) {
    let dropped = 0;
    const before = html;

    // Match any <link …cdnjs.cloudflare.com…font-awesome…> whole line
    // (plain stylesheet, async with media=print onload=…, or standalone).
    html = html.replace(
        /^[ \t]*<link\s+[^>]*cdnjs\.cloudflare\.com\/ajax\/libs\/font-awesome\/[^>]*>\n/gm,
        () => { dropped++; return ''; }
    );
    // Match the <noscript> fallback form as a whole line.
    html = html.replace(
        /^[ \t]*<noscript><link[^>]*cdnjs\.cloudflare\.com\/ajax\/libs\/font-awesome\/[^>]*><\/noscript>\n/gm,
        () => { dropped++; return ''; }
    );
    // Drop the dns-prefetch line for cdnjs (cdnjs is only used for FA here).
    html = html.replace(
        /^[ \t]*<link\s+rel="dns-prefetch"\s+href="https:\/\/cdnjs\.cloudflare\.com"[^>]*>\n/gm,
        () => { dropped++; return ''; }
    );

    return { result: html, dropped, changed: html !== before };
}

// Inline the SVG sprite inside <body> so every `<svg><use href="#fa-*"/>`
// resolves without a network hop. Idempotent via marker-bracketed block;
// the stored `data-hash` lets browsers (and us) see when the sprite churned.
function injectSprite(html, spriteInfo) {
    const block =
        `${SPRITE_BEGIN}\n` +
        `<div hidden data-hash="${spriteInfo.hash}">${spriteInfo.svg}</div>\n` +
        `${SPRITE_END}`;

    const existing = new RegExp(
        escapeRegex(SPRITE_BEGIN) + '[\\s\\S]*?' + escapeRegex(SPRITE_END),
        'g'
    );
    if (existing.test(html)) {
        const replaced = html.replace(existing, block);
        return { result: replaced, injected: false, replaced: true };
    }

    // Insert immediately after the opening <body …> tag.
    const anchor = /<body\b[^>]*>\n?/;
    const m = html.match(anchor);
    if (!m) return { result: html, injected: false, replaced: false };
    const insertAt = m.index + m[0].length;
    const result = html.slice(0, insertAt) + block + '\n' + html.slice(insertAt);
    return { result, injected: true, replaced: false };
}

// Load the sprite once per stamp run. Stripped of the leading <?xml?> + the
// outer comment so the inlined form reads as ordinary markup inside <body>.
function loadSprite() {
    const abs = path.join(REPO_ROOT, SPRITE_SOURCE);
    if (!fs.existsSync(abs)) {
        throw new Error(`sprite missing at ${abs} — run: npm run build:icons`);
    }
    const raw = fs.readFileSync(abs, 'utf8');
    const hash = crypto.createHash('sha256').update(raw).digest('hex').slice(0, 8);
    // Trim the XML prolog + generator comment for cleaner HTML output;
    // browsers don't need xmlns on a child <svg> inside <body>.
    const svg = raw
        .replace(/^<\?xml[^?]*\?>\s*/, '')
        .replace(/^<!--[\s\S]*?-->\s*/, '')
        .trim();
    return { svg, hash };
}

function asyncifyStylesheets(html) {
    const conversions = [];

    // Match <link rel="stylesheet" ...> where href points to a .min.css (local
    // post-stamp form). href may appear before or after rel. The attribute
    // order in stamped files is `rel="stylesheet" href="..."` so that's what
    // we optimize for. Exclude lines inside <noscript> via a lookbehind-free
    // check: require the match to start at an indent, not immediately after
    // <noscript>.
    const re = /^([ \t]*)<link rel="stylesheet" href="([^"]*\.min\.css\?v=[a-f0-9]+)">/gm;

    const result = html.replace(re, (match, indent, href, offset) => {
        // Skip if the match is inside a <noscript>…</noscript> block. We look
        // for the nearest <noscript> tag before `offset` without a closing
        // </noscript> between it and the match.
        const before = html.slice(0, offset);
        const lastOpen = before.lastIndexOf('<noscript');
        const lastClose = before.lastIndexOf('</noscript>');
        if (lastOpen > lastClose) return match;

        conversions.push(href);
        return (
            `${indent}<link rel="preload" as="style" href="${href}" onload="this.onload=null;this.rel='stylesheet'">\n` +
            `${indent}<noscript><link rel="stylesheet" href="${href}"></noscript>`
        );
    });

    return { result, conversions };
}

const I_TAG_RE = /<i\s+class=(["'])([^"']*?)\1([^>]*)><\/i>/g;
const FA_MODIFIERS = new Set(['spin', 'pulse', 'flip', 'flip-horizontal', 'flip-vertical']);
const FA_DROP_PREFIXES = new Set(['fas', 'fab', 'far', 'fa']);

function extractIconName(classAttr) {
    const tokens = classAttr.split(/\s+/).filter(Boolean);
    for (const token of tokens) {
        const match = token.match(/^fa-([a-z][a-z0-9-]*)$/);
        if (!match) continue;
        const name = match[1];
        if (FA_MODIFIERS.has(name)) continue;
        if (/^[0-9]x$/.test(name)) continue;
        if (/^rotate-\d+$/.test(name)) continue;
        return name;
    }
    return null;
}

function rewriteFaClass(classAttr, iconName) {
    const kept = ['icon'];
    for (const token of classAttr.split(/\s+/).filter(Boolean)) {
        if (FA_DROP_PREFIXES.has(token)) continue;
        if (token === `fa-${iconName}`) continue;
        kept.push(token);
    }
    return kept.join(' ');
}

function migrateFaIcons(html) {
    let changes = 0;
    const result = html.replace(I_TAG_RE, (match, quote, classAttr, rest) => {
        const iconName = extractIconName(classAttr);
        if (!iconName) return match;

        const trailing = rest.trim() ? ` ${rest.trim()}` : '';
        const ariaHidden = /aria-hidden=/.test(rest) ? '' : ' aria-hidden="true"';
        changes++;
        return `<svg class="${rewriteFaClass(classAttr, iconName)}"${ariaHidden}${trailing}><use href="#fa-${iconName}"/></svg>`;
    });

    return { result, changes };
}

function ensureWebVitalsBeacon(html, manifest) {
    const info = manifest[WEB_VITALS_SOURCE];
    if (!info) {
        throw new Error(`manifest missing entry for ${WEB_VITALS_SOURCE} — run build:assets:minify`);
    }

    const src = `/${info.min}?v=${info.hash}`;
    if (html.includes(src)) {
        return { result: html, injected: false };
    }

    const analyticsLine = /^([ \t]*)<script defer src="\/scripts\/analytics\.min\.js\?v=[a-f0-9]+"><\/script>\n?/m;
    const match = html.match(analyticsLine);
    if (!match) {
        return { result: html, injected: false };
    }

    const indent = match[1] || '    ';
    const insertAt = (match.index ?? 0) + match[0].length;
    const result = html.slice(0, insertAt) + `${indent}<script defer src="${src}"></script>\n` + html.slice(insertAt);
    return { result, injected: true };
}

function normalizeCloudflareEmail(html) {
    let changes = 0;
    let result = html.replace(/href="\/cdn-cgi\/l\/email-protection#[^"]+"/g, () => {
        changes++;
        return 'href="mailto:myf1stories@gmail.com"';
    });

    result = result.replace(
        /^[ \t]*<script\s+data-cfasync="false"\s+src="\/cdn-cgi\/scripts\/[^"]+\/cloudflare-static\/email-decode\.min\.js"><\/script>\n?/gm,
        () => {
            changes++;
            return '';
        }
    );

    return { result, changes };
}

function stampArticleShell({ patterns, bootstrapInfo, fontsInfo, critical, sprite, manifest }, dry) {
    const totals = {
        files: 0,
        stamps: 0,
        bootstrapCssSwaps: 0,
        bootstrapCssDrops: 0,
        bootstrapJsDrops: 0,
        bootstrapPreconnectDrops: 0,
        noscriptFixes: 0,
        fontSwaps: 0,
        fontPreconnectDrops: 0,
        fontPreloads: 0,
        faCdnDrops: 0,
        iconTags: 0,
        spriteOps: 0,
        criticalOps: 0,
        asyncified: 0,
        webVitals: 0,
        cloudflareEmail: 0
    };

    for (const rel of listArticleHtml()) {
        const abs = path.join(REPO_ROOT, rel);
        const original = fs.readFileSync(abs, 'utf8');

        let { result, hits } = rewrite(original, patterns);
        totals.stamps += hits.length;

        const bootstrapSwap = swapBootstrapCdn(result, bootstrapInfo);
        result = bootstrapSwap.result;
        totals.bootstrapCssSwaps += bootstrapSwap.swaps.cssSwapped;
        totals.bootstrapCssDrops += bootstrapSwap.swaps.cssDropped;
        totals.bootstrapJsDrops += bootstrapSwap.swaps.jsDropped;
        totals.bootstrapPreconnectDrops += bootstrapSwap.swaps.preconnectDropped;

        const noscriptCleanup = normalizeStylesheetNoscriptFallbacks(result);
        result = noscriptCleanup.result;
        totals.noscriptFixes += noscriptCleanup.fixes;

        const criticalSwap = injectCritical(result, critical);
        result = criticalSwap.result;
        if (criticalSwap.injected || criticalSwap.replaced) totals.criticalOps++;

        const fontSwap = swapFonts(result, ARTICLE_SHELL_CONTEXT, fontsInfo, manifest);
        result = fontSwap.result;
        totals.fontSwaps += fontSwap.swaps.googleLinksSwapped;
        totals.fontPreconnectDrops += fontSwap.swaps.preconnectDropped;
        totals.fontPreloads += fontSwap.swaps.preloadsInjected;

        const cdnDrop = dropFontAwesomeCdn(result);
        result = cdnDrop.result;
        totals.faCdnDrops += cdnDrop.dropped;

        const iconMigration = migrateFaIcons(result);
        result = iconMigration.result;
        totals.iconTags += iconMigration.changes;

        const spriteSwap = injectSprite(result, sprite);
        result = spriteSwap.result;
        if (spriteSwap.injected || spriteSwap.replaced) totals.spriteOps++;

        const vitals = ensureWebVitalsBeacon(result, manifest);
        result = vitals.result;
        if (vitals.injected) totals.webVitals++;

        const cloudflare = normalizeCloudflareEmail(result);
        result = cloudflare.result;
        totals.cloudflareEmail += cloudflare.changes;

        const asyncSwap = asyncifyStylesheets(result);
        result = asyncSwap.result;
        totals.asyncified += asyncSwap.conversions.length;

        if (result === original) continue;
        totals.files++;
        if (!dry) fs.writeFileSync(abs, result, 'utf8');
    }

    return totals;
}

function main() {
    const manifest = loadManifest();
    const patterns = buildPatternsFromManifest(manifest);
    const critical = loadCriticalCss(manifest);
    const fontsInfo = manifest[FONTS_SOURCE];
    if (!fontsInfo) {
        throw new Error(`manifest missing entry for ${FONTS_SOURCE} — run build:fonts then build:assets:minify`);
    }
    const bootstrapInfo = manifest[BOOTSTRAP_SOURCE];
    if (!bootstrapInfo) {
        throw new Error(`manifest missing entry for ${BOOTSTRAP_SOURCE} — run build:bootstrap then build:assets:minify`);
    }
    const sprite = loadSprite();
    const dry = process.argv.includes('--dry');
    let totalHits = 0;
    let totalCriticalOps = 0;
    let totalAsyncified = 0;
    let totalFontSwaps = 0;
    let totalPreconnectDrops = 0;
    let totalSpriteOps = 0;
    let totalFaCdnDrops = 0;
    let totalBootstrapCssSwaps = 0;
    let totalBootstrapCssDrops = 0;
    let totalBootstrapJsDrops = 0;
    let totalBootstrapPreconnectDrops = 0;
    let totalNoscriptFixes = 0;

    for (const rel of TARGET_HTML) {
        const abs = path.join(REPO_ROOT, rel);
        if (!fs.existsSync(abs)) {
            console.warn(`skip (missing): ${rel}`);
            continue;
        }
        const original = fs.readFileSync(abs, 'utf8');

        // 1) stamp local asset refs with content-hash query strings
        let { result, hits } = rewrite(original, patterns);

        // 1b) swap Bootstrap CDN CSS → local slim build and drop the unused
        //     Bootstrap JS bundle.
        const bootstrapSwap = swapBootstrapCdn(result, bootstrapInfo);
        result = bootstrapSwap.result;
        totalBootstrapCssSwaps += bootstrapSwap.swaps.cssSwapped;
        totalBootstrapCssDrops += bootstrapSwap.swaps.cssDropped;
        totalBootstrapJsDrops += bootstrapSwap.swaps.jsDropped;
        totalBootstrapPreconnectDrops += bootstrapSwap.swaps.preconnectDropped;

        const noscriptCleanup = normalizeStylesheetNoscriptFallbacks(result);
        result = noscriptCleanup.result;
        totalNoscriptFixes += noscriptCleanup.fixes;

        // 2) swap Google Fonts → self-hosted (only on pages that had them)
        const fontSwap = swapFonts(result, rel, fontsInfo, manifest);
        result = fontSwap.result;
        totalFontSwaps += fontSwap.swaps.googleLinksSwapped;
        totalPreconnectDrops += fontSwap.swaps.preconnectDropped;

        // 2b) Phase 3b: drop FA CDN <link>s + inline sprite (only on pages
        //     that use icons; offline/404 have none).
        let faDropNote = '';
        let spriteNote = '';
        if (SPRITE_TARGETS.has(rel)) {
            const cdnDrop = dropFontAwesomeCdn(result);
            result = cdnDrop.result;
            totalFaCdnDrops += cdnDrop.dropped;
            if (cdnDrop.dropped) faDropNote = `${cdnDrop.dropped} FA CDN drop`;

            const inj = injectSprite(result, sprite);
            result = inj.result;
            if (inj.injected) { spriteNote = 'sprite injected'; totalSpriteOps++; }
            else if (inj.replaced) { spriteNote = 'sprite refreshed'; totalSpriteOps++; }
        }

        // 3) inject/refresh critical-CSS block + asyncify local stylesheets
        let criticalNote = '';
        let conversions = [];
        if (CRITICAL_TARGETS.has(rel)) {
            const inj = injectCritical(result, critical);
            result = inj.result;
            if (inj.injected) { criticalNote = 'injected'; totalCriticalOps++; }
            else if (inj.replaced) { criticalNote = 'refreshed'; totalCriticalOps++; }
            else criticalNote = 'skipped (no anchor)';

            const out = asyncifyStylesheets(result);
            result = out.result;
            conversions = out.conversions;
            totalAsyncified += conversions.length;
        }

        totalHits += hits.length;
        const changed = result !== original;
        if (!changed) {
            console.log(`· ${rel}  (no changes)`);
            continue;
        }
        const parts = [];
        if (hits.length) parts.push(`${hits.length} stamp(s)`);
        if (bootstrapSwap.swaps.cssSwapped) parts.push(`${bootstrapSwap.swaps.cssSwapped} Bootstrap swap`);
        if (bootstrapSwap.swaps.cssDropped) parts.push(`${bootstrapSwap.swaps.cssDropped} Bootstrap drop`);
        if (bootstrapSwap.swaps.jsDropped) parts.push(`${bootstrapSwap.swaps.jsDropped} Bootstrap JS drop`);
        if (bootstrapSwap.swaps.preconnectDropped) parts.push(`${bootstrapSwap.swaps.preconnectDropped} jsDelivr preconnect drop`);
        if (criticalNote && criticalNote !== 'skipped (no anchor)') parts.push(`critical ${criticalNote}`);
        if (conversions.length) parts.push(`${conversions.length} async-preload`);
        if (fontSwap.swaps.googleLinksSwapped) parts.push(`${fontSwap.swaps.googleLinksSwapped} font swap`);
        if (fontSwap.swaps.preconnectDropped) parts.push(`${fontSwap.swaps.preconnectDropped} preconnect drop`);
        if (fontSwap.swaps.preloadsInjected) parts.push(`${fontSwap.swaps.preloadsInjected} font preload`);
        if (faDropNote) parts.push(faDropNote);
        if (spriteNote) parts.push(spriteNote);
        console.log(`\n${rel}  →  ${parts.join(', ') || 'updated'}`);
        for (const h of hits) {
            console.log(`    ${h.from}\n  → ${h.to}`);
        }
        for (const href of conversions) {
            console.log(`    async: ${href}`);
        }
        if (!dry) {
            fs.writeFileSync(abs, result, 'utf8');
        }
    }

    const articleShell = stampArticleShell(
        { patterns, bootstrapInfo, fontsInfo, critical, sprite, manifest },
        dry
    );
    totalHits += articleShell.stamps;
    totalBootstrapCssSwaps += articleShell.bootstrapCssSwaps;
    totalBootstrapCssDrops += articleShell.bootstrapCssDrops;
    totalBootstrapJsDrops += articleShell.bootstrapJsDrops;
    totalBootstrapPreconnectDrops += articleShell.bootstrapPreconnectDrops;
    totalNoscriptFixes += articleShell.noscriptFixes;
    totalFontSwaps += articleShell.fontSwaps;
    totalPreconnectDrops += articleShell.fontPreconnectDrops;
    totalSpriteOps += articleShell.spriteOps;
    totalFaCdnDrops += articleShell.faCdnDrops;
    totalCriticalOps += articleShell.criticalOps;
    totalAsyncified += articleShell.asyncified;
    if (articleShell.files) {
        console.log(
            `\n${ARTICLE_HTML_ROOT}/**/article.html  →  ${articleShell.files} file(s), ` +
            `${articleShell.stamps} stamp(s), ` +
            `${articleShell.noscriptFixes} noscript fix(es), ` +
            `${articleShell.fontSwaps} font swap(s), ` +
            `${articleShell.faCdnDrops} FA CDN drop(s), ` +
            `${articleShell.iconTags} icon tag(s), ` +
            `${articleShell.webVitals} web-vitals insert(s), ` +
            `${articleShell.asyncified} async-preload conversion(s), ` +
            `${articleShell.cloudflareEmail} Cloudflare email cleanup(s)`
        );
    }

    if (dry) {
        console.log(
            `\n(dry-run) ${totalHits} rewrite(s), ${totalCriticalOps} critical op(s), ` +
            `${totalAsyncified} async-preload conversion(s), ${totalFontSwaps} font swap(s), ` +
            `${totalPreconnectDrops} preconnect drop(s), ${totalSpriteOps} sprite op(s), ` +
            `${totalFaCdnDrops} FA CDN drop(s), ${totalBootstrapCssSwaps} Bootstrap CSS swap(s), ` +
            `${totalBootstrapCssDrops} Bootstrap CSS drop(s), ${totalBootstrapJsDrops} Bootstrap JS drop(s), ` +
            `${totalBootstrapPreconnectDrops} jsDelivr preconnect drop(s), ${totalNoscriptFixes} noscript fix(es) planned.`
        );
    } else {
        console.log(
            `\n✓ stamped ${totalHits} reference(s), ${totalCriticalOps} critical block(s), ` +
            `${totalAsyncified} stylesheet(s) async-preloaded, ${totalFontSwaps} font swap(s), ` +
            `${totalPreconnectDrops} preconnect(s) dropped, ${totalSpriteOps} sprite op(s), ` +
            `${totalFaCdnDrops} FA CDN drop(s), ${totalBootstrapCssSwaps} Bootstrap CSS swap(s), ` +
            `${totalBootstrapCssDrops} Bootstrap CSS drop(s), ${totalBootstrapJsDrops} Bootstrap JS drop(s), ` +
            `${totalBootstrapPreconnectDrops} jsDelivr preconnect drop(s), ${totalNoscriptFixes} noscript fix(es) across ${TARGET_HTML.length} shell file(s).`
        );
    }
}

main();
